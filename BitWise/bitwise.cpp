#include<bits/stdc++.h>
using namespace std;
 
int main()
{
    int a=4;
    int b=2;
    cout<<"a & b = "<<(a&b)<<endl;
    cout<<"a | b = "<<(a|b)<<endl;
    cout<<"a ^ b = "<<(a^b)<<endl;
    cout<<"~a = "<<(~a)<<endl;
    cout<<"~b = "<<(~b)<<endl;

    return 0;
}