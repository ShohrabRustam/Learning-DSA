#include<bits/stdc++.h>
using namespace std;
 
int main()
{
 int a = 21;
 cout <<"a << 2 = " << (a<<2) <<endl;
 cout <<"a << 1 = " << (a<<1) <<endl;
 cout <<"a << 3 = " << (a<<3) <<endl;
 cout <<"a << 6 = " << (a<<6) <<endl;

 cout <<"a >> 2 = " << (a>>2) <<endl;
 cout <<"a >> 1 = " << (a>>1) <<endl;
 cout <<"a >> 3 = " << (a>>3) <<endl;
 cout <<"a >> 6 = " << (a>>6) <<endl;
    return 0;
}