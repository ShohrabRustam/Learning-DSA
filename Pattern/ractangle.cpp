#include<bits/stdc++.h>
using namespace std;
void make_rectangle(){
    int row, col;
    cout <<"Enter number of rows and columns"<<endl;
    cin >> row >> col;
    for (int i=0; i < row;i++){
        for (int j=0; j < col;j++){
            cout << " * ";
        }
        cout << endl;
    }
}
 
int main()
{
    make_rectangle();
    return 0;
}