#include<bits/stdc++.h>
using namespace std;

void leftRightAngleTriangle(int row){
    for(int i=0;i<row;i++){
        for(int j=0;j<row;j++){
            if(j>=row-i-1){
            cout<<" * ";
            }else{
                cout<<"   ";
            }
        }
        cout<<endl;
    }
}

int main()
{
 int row;
 cin >> row;
 leftRightAngleTriangle(row);
    return 0;
}