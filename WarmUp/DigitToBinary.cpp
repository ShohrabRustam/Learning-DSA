#include<bits/stdc++.h>
using namespace std;
 
int main()
{
    int n;
    cin >> n;
    string binaryNumber = "";
    while(n){
            binaryNumber = to_string(n%2)+binaryNumber; 
            n=n/2;
    }
    cout << binaryNumber;
    return 0;
}