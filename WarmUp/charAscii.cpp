#include<bits/stdc++.h>
using namespace std;
 
int main()
{
  char c;
  cin >> c;
  if(c>='0' && c<='9'){
    cout<<"this is the Number"<<endl;
  }else if(c>='a' && c<='z') {
    cout<<"this is the lowercase string"<<endl;
  } else if(c>='A' && c<='Z'){
    cout<<"this is the uppercase string"<<endl;
  }
    return 0;
}