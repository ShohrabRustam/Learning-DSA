#include<bits/stdc++.h>
using namespace std;
 long long int power(int number,int p){
    if(p==0){
        return 1;
    }
    return number*power(number,p-1);
 }
int main()
{
    int n;
    cin>>n;
    int p;
    cin>>p;
    cout<<power(n,p)<<endl;
 
    return 0;
}