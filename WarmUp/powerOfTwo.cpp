#include<bits/stdc++.h>
using namespace std;
 
int main()
{
 int n;
 cin >>n;
 if(n==0){
    cout <<"Yes"<<endl;
    return 1;
 }
 while(n!=1){
    if(n%2!=0){
        cout <<"No"<<endl;
        return 0;
    }

    n=n/2;
 }
 cout<<"Yes"<<endl;
    return 1;
}