#include<bits/stdc++.h>
using namespace std;
 
int main()
{
 int n;
 cin>>n;
 int product = 1;
 int sum = 0;
 while(n){
    int rem = n%10;
    sum += rem;
    product*=rem;
    n=n/10;
 }
 cout<<sum-product<<endl;
    return 0;
}